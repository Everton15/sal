
package jcu.sal.xml;

public class JaxbException extends XmlException {

	public JaxbException() {
		super();
	}

	public JaxbException(String message) {
		super(message);
	}

	public JaxbException(String message, Throwable cause) {
		super(message, cause);
	}

	public JaxbException(Throwable cause) {
		super(cause);
	}

}
